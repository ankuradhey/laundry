    }

    /**
     * Fetch a resource
     *
     * @param  mixed $id
     * @return ApiProblem|mixed
     */
    public function fetch($id)
    {
        $e = $this->getEvent();
        $route = $e->getRouteMatch();
        $orderStatus = $route->getParam('status_type');
        
        //check if delivery boy is there
        $res = $this->mapper->checkDeliveryBoy($id);
        if(!$res)
            return new ApiProblem(405, 'Delivery boy not found');
            
        //get orders
        return $this->mapper->fetchOrders($id, $orderStatus);
        
    }

    /**
     * Fetch all or a subset of resources
