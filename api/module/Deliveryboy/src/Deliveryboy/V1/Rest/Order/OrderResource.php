<?php
namespace Deliveryboy\V1\Rest\Order;

use ZF\ApiProblem\ApiProblem;
use ZF\Rest\AbstractResourceListener;

class OrderResource extends AbstractResourceListener
{
    protected $mapper;
    
    public function __construct($mapper){
        $this->mapper = $mapper;
    }
